    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:                                      
        if (selectedTransaksiId > 0) {
            try {
                String rawNominal = txtNominal.getText().replace(",", "");
                double nominal = Double.parseDouble(rawNominal);
                String keterangan = txtKet.getText();
                String jenis = cbJenis.getSelectedItem().toString();
                String event = cbEvent.getSelectedItem().toString();
                String day = cbTgl.getSelectedItem().toString();
                String month = cbBln.getSelectedItem().toString();
                String year = cbThn.getSelectedItem().toString();

                String tanggalStr = year + "-" + month + "-" + String.format("%02d", Integer.parseInt(day));
                java.sql.Date tanggalSql = java.sql.Date.valueOf(tanggalStr);

                Transaksi t = new Transaksi(selectedTransaksiId, tanggalSql, jenis, event, nominal, keterangan, Session.userId);
                
                transaksiController tc = new transaksiController();
                if (tc.editTransaksi(t)) {
                    JOptionPane.showMessageDialog(this, "Transaksi berhasil diupdate!");
                    tc.loadData(model);
                    hitungUlangSaldo();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal update transaksi.");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris transaksi dulu!");
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void tblTransaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTransaksiMouseClicked
        // TODO add your handling code here:                                         
        int row = tblTransaksi.getSelectedRow();
        if (row >= 0) {
            // Ambil data dari tabel
            selectedTransaksiId = (int) model.getValueAt(row, 0); // kolom 0 = transaksi_id
            String tanggal = model.getValueAt(row, 1).toString();
            String jenis = model.getValueAt(row, 2).toString();
            String event = model.getValueAt(row, 3).toString();
            String nominal = model.getValueAt(row, 4).toString();
            String ket = model.getValueAt(row, 5).toString();

            // Set ke form
            txtNominal.setText(nominal);
            txtKet.setText(ket);
            cbJenis.setSelectedItem(jenis);
            cbEvent.setSelectedItem(event);

            // Pecah tanggal yyyy-MM-dd ke cbTgl, cbBln, cbThn
            String[] parts = tanggal.split("-");
            cbThn.setSelectedItem(parts[0]);
            cbBln.setSelectedItem(parts[1]);
            cbTgl.setSelectedItem(parts[2]);
        }
    }//GEN-LAST:event_tblTransaksiMouseClicked

