    private void hitungUlangSaldo() {
        double totalPemasukan = 0;
        double totalPengeluaran = 0;

        for (int i = 0; i < model.getRowCount(); i++) {
            try {
                String jenis = model.getValueAt(i, 2).toString(); // kolom 2 = Jenis Transaksi
                Object value = model.getValueAt(i, 4);            // kolom 4 = Nominal

                double nominal;
                if (value instanceof Number) {
                    nominal = ((Number) value).doubleValue();
                } else {
                    nominal = Double.parseDouble(value.toString().replace(".", ""));
                }

                if (jenis.equalsIgnoreCase("Pemasukan")) {
                    totalPemasukan += nominal;
                } else {
                    totalPengeluaran += nominal;
                }
            } catch (Exception e) {
                System.out.println("Error hitung baris ke-" + i + ": " + e.getMessage());
            }
        }

        double saldoAkhir = totalPemasukan - totalPengeluaran;
        String formatted = String.format("%,.0f", saldoAkhir).replace(',', '.');
        lblSaldo.setText("Rp " + formatted);
        lblSaldo.setForeground(saldoAkhir < 0 ? java.awt.Color.RED : new java.awt.Color(0, 153, 51));
    }
    
