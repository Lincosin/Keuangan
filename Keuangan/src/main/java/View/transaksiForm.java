    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:                                        
    try {
        // Ambil nominal dari textfield, hapus titik ribuan dulu
        String rawNominal = txtNominal.getText().replaceAll("\\.", "");
        double nominal = Double.parseDouble(rawNominal);

        String keterangan = txtKet.getText();
        String jenis = cbJenis.getSelectedItem().toString();
        String event = cbEvent.getSelectedItem().toString();
        String day = cbTgl.getSelectedItem().toString();
        String month = cbBln.getSelectedItem().toString();
        String year = cbThn.getSelectedItem().toString();

        // Gabungkan jadi format yyyy-MM-dd
        String tanggalStr = year + "-" + month + "-" + String.format("%02d", Integer.parseInt(day));
        java.sql.Date tanggalSql = java.sql.Date.valueOf(tanggalStr);

        // Buat objek Transaksi
        Transaksi t = new Transaksi(1, tanggalSql, jenis, event, nominal, keterangan);

        // Simpan via controller
        transaksiController tc = new transaksiController();
        if (tc.tambahTransaksi(t)) {
            JOptionPane.showMessageDialog(this, "Transaksi berhasil disimpan!");
            tc.loadData(model); // refresh JTable
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan transaksi.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void txtNominalKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNominalKeyReleased
        // TODO add your handling code here:                                        
    try {
        // Ambil teks, hapus titik ribuan
        String originalText = txtNominal.getText().replaceAll("\\.", "");
        if (originalText.isEmpty()) return;

        // Ubah ke angka
        double nominal = Double.parseDouble(originalText);

        // Format ulang dengan titik ribuan (Indonesia style)
        String formattedText = String.format("%,d", (long) nominal).replace(',', '.');

        // Set kembali ke textfield
        txtNominal.setText(formattedText);

    } catch (NumberFormatException e) {
        // Abaikan jika bukan angka
    }

    }//GEN-LAST:event_txtNominalKeyReleased

