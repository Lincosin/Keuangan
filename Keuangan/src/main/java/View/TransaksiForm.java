    private void loadData() {
    model.setRowCount(0); 
    try {
        Connection conn = Koneksi.sambung();
        Statement st = conn.createStatement();
        // Mengurutkan berdasarkan ID terbaru agar transaksi terakhir ada di atas
        String sql = "SELECT * FROM transaksi ORDER BY id_transaksi DESC";
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_transaksi"),      // Kolom 0
                rs.getString("tgl_transaksi"),  // Kolom 1
                rs.getString("jenis_transaksi"),// Kolom 2
                rs.getString("event"),          // Kolom 3
                rs.getDouble("nominal"),        // Kolom 4
                rs.getString("keterangan")      // Kolom 5
            });
        }
        hitungUlangSaldo();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
    }
}

