    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
        String tgl = datePicker1.getDateStringOrEmptyString();
        String jns = cbJenis.getSelectedItem().toString();
        String evtName = cbEvent.getSelectedItem().toString();
        String ket = txtKeterangan.getText();
        String nomClean = txtNominal.getText().replaceAll("\\.", "");

        if (tgl.isEmpty() || nomClean.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tanggal dan Nominal wajib diisi!");
            return;
        }

        double nom = Double.parseDouble(nomClean);

        Connection conn = Koneksi.sambung();
        // ID tidak dimasukkan karena AUTO_INCREMENT
        String sql = "INSERT INTO transaksi (tgl_transaksi, jenis_transaksi, event, nominal, keterangan) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        
        pst.setString(1, tgl);
        pst.setString(2, jns);
        pst.setString(3, evtName);
        pst.setDouble(4, nom);
        pst.setString(5, ket);

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan!");

        loadData(); // Memanggil ulang data agar ID muncul di tabel
        
        // Reset Form
        txtKeterangan.setText("");
        txtNominal.setText("");
        datePicker1.setDateToToday();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal Simpan: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void txtNominalKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNominalKeyReleased
        try {
        // 1. Ambil teks, hapus semua titik yang ada agar bisa dihitung ulang
        String originalText = txtNominal.getText().replaceAll("\\.", "");
        if (originalText.isEmpty()) return;

        // 2. Ubah ke angka
        double nominal = Double.parseDouble(originalText);

        // 3. Format ulang dengan titik pemisah ribuan (Format Indonesia)
        String formattedText = String.format("%,.0f", nominal).replace(',', '.');

        // 4. Set kembali ke textfield
        txtNominal.setText(formattedText);
        
    } catch (NumberFormatException e) {
        // Abaikan jika bukan angka
    }
    }//GEN-LAST:event_txtNominalKeyReleased

