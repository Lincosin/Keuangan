    private void hitungUlangSaldo() {
    double totalPemasukan = 0;
    double totalPengeluaran = 0;

    for (int i = 0; i < model.getRowCount(); i++) {
        try {
            // Ambil data Jenis (Index 2)
            String jenis = model.getValueAt(i, 2).toString();
            
            // Ambil data Nominal (Index 4), hapus titik jika ada agar bisa diparse
            String nomString = model.getValueAt(i, 4).toString().replace(".", "");
            double nominal = Double.parseDouble(nomString);

            if (jenis.equalsIgnoreCase("Pemasukan")) {
                totalPemasukan += nominal;
            } else {
                totalPengeluaran += nominal;
            }
        } catch (Exception e) {
            // Lewati jika ada baris yang error/kosong
            System.out.println("Error hitung baris ke-" + i + ": " + e.getMessage());
        }
    }

    double saldoAkhir = totalPemasukan - totalPengeluaran;
    
    // Format tampilan ke Rupiah dengan titik
    String formatted = String.format("%,.0f", saldoAkhir).replace(',', '.');
    lblBalance.setText("Rp " + formatted);
    
    // Ubah warna teks (Merah jika minus, Hijau jika surplus)
    lblBalance.setForeground(saldoAkhir < 0 ? java.awt.Color.RED : new java.awt.Color(0, 153, 51));
}
    
